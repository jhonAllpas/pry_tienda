    private void btnVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenderActionPerformed
        if(detalleProductos.size()<1){
            JOptionPane.showMessageDialog(null,"Debe ingresar productos");
            return;
        }
        if(proveedor.getIdproveedor()==null){
            JOptionPane.showMessageDialog(null,"Debe seleccionar proveedor");
            return;
        }
        M_Compra compra = new M_Compra();
        compra.setFecha(dcfecha.getDate());
        compra.setNum_documento(txtNumComprobante.getText());
        compra.setTipo_documento(cboTipoDocumento.getSelectedItem().toString());
        compra.setSubtotal(Double.parseDouble(txtSubTotal.getText()));
        compra.setIgv(Double.parseDouble(txtIGV.getText()));
        compra.setTotal(Double.parseDouble(txtTotal.getText()));
        compra.setEstado(cboEstado.getSelectedItem().toString());
        compra.setIdusuario(current_usuario.getId());
        compra.setIdProveedor(proveedor.getRuc());
        compra.setDetalle(detalleProductos);

        new N_Compra().insertar(compra);
        JOptionPane.showMessageDialog(null, "Compra Ingresada");
        cboEstado.setSelectedIndex(0);
        cboTipoDocumento.setSelectedIndex(0);
        txtSubTotal.setText("");
        txtIGV.setText("");
        txtTotal.setText("");
        detalleProductos = new ArrayList<M_Detalle>();
        
        proveedor = new M_Proveedor();
        btnProveedor.setText("Seleccionar");
    }//GEN-LAST:event_btnVenderActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        try {
            
            int cantidad = Integer.parseInt(tblListado.getModel().getValueAt(tblListado.getSelectedRow(), 3).toString());
            String idProducto = tblListado.getModel().getValueAt(tblListado.getSelectedRow(), 0).toString();
            
            detalleProductos = detalleProductos.stream().map(det->{
                if(det.getIdproducto().equals(idProducto)){
                    det.setCantidad(cantidad);
                    det.setTotal(cantidad*det.getPrecio());
                }
                return det;
            }).collect(Collectors.toList());
            
            mostrarDetalleCompra();
            mostrarTotales();
        } catch (NumberFormatException ex) {
            JOptionPane.showConfirmDialog(null, "Debe ingresar un valor numérico en Cantidad.");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

