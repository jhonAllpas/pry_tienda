    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        if(txtidproveedor.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Ingrese codigo del proveedor");
            txtidproveedor.requestFocus();
            return;
        }
        if(txtrazonsocial.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Ingrese razon social");
            txtrazonsocial.requestFocus();
            return;
        }
        if(txtdireccion.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Ingrese direccion");
            txtdireccion.requestFocus();
            return;
        }
        if(txttelefono.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Ingrese telefono del proveedor");
            txttelefono.requestFocus();
            return;
        }
        if(txtruc.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Ingrese RUC del proveedor");
            txtruc.requestFocus();
            return;
        }
        M_Proveedor dts = new M_Proveedor();
        N_Proveedor func = new N_Proveedor();

        dts.setIdproveedor(txtidproveedor.getText());
        dts.setRazonsocial(txtrazonsocial.getText());
        dts.setDireccion(txtdireccion.getText());
        dts.setTelefono(txttelefono.getText());
        dts.setRuc(txtruc.getText());

        if(accion.equals("guardar")){
            if (func.insertar(dts)){
                JOptionPane.showConfirmDialog(rootPane, "Proveedor registrado");
                mostrar("");
                limpiar();
                inhabilitar();
            }
        } else if (accion.equals("editar")){
            if(func.editar(dts))
            JOptionPane.showConfirmDialog(rootPane, "el cliente fue modificado");
            mostrar("");
            limpiar();
            inhabilitar();
        }

    }//GEN-LAST:event_btnguardarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        M_Proveedor dts = new M_Proveedor();
        N_Proveedor func = new N_Proveedor();

        if(!txtidproveedor.getText().equals("")){
            int confirmacion= JOptionPane.showConfirmDialog(rootPane, "Desea eliminar el proveedor","confirmar",2);
            if(confirmacion==0){
                dts.setIdproveedor(txtidproveedor.getText());
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

