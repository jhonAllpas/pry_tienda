    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        btnguardar.setText("Editar");
        habilitar();
        btneliminar.setEnabled(true);
        accion="editar";

        int fila = tablalistado.rowAtPoint(evt.getPoint());

        txtidproveedor.setText(tablalistado.getValueAt(fila,0).toString());
        txtrazonsocial.setText(tablalistado.getValueAt(fila,1).toString());
        txtruc.setText(tablalistado.getValueAt(fila,2).toString());
        txttelefono.setText(tablalistado.getValueAt(fila,3).toString());
        txtdireccion.setText(tablalistado.getValueAt(fila,4).toString());

    }//GEN-LAST:event_tablalistadoMouseClicked


