    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        if (txtIdEmpleado.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese nombre del empleado");
            txtIdEmpleado.requestFocus();
            return;
        }
        if (txtIdUsuario.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese apellido del empleado");
            txtIdUsuario.requestFocus();
            return;
        }

        if (txtUsuario.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese telÃ©fono del empleado");
            txtUsuario.requestFocus();
            return;
        }
        if (txtContrasena.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese contraseÃ±a para usuario");
            txtContrasena.requestFocus();
            return;
        }
        if (txtCorreo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese correo para usuario");
            txtCorreo.requestFocus();
            return;
        }
        M_Usuario dts = new M_Usuario();
        N_Usuario func = new N_Usuario();

        dts.setId(Integer.parseInt(txtIdUsuario.getText()));
        dts.setIdEmpleado(txtIdEmpleado.getText());
        dts.setUsuario(txtUsuario.getText());
        dts.setAcceso(cboAcceso.getSelectedItem().toString());
        dts.setEstado(cboEstado.getSelectedItem().toString());
                dts.setPass(txtContrasena.getText());
               dts.setCorreo(txtCorreo.getText());

        if (accion.equals("guardar")) {

            if (func.insertar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "usuario registrado");
                mostrar("");
                limpiar();
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
//            if (txtContrasena.getText() != null && txtContrasena.getText().length() > 0) {
//                dts.setPass(txtContrasena.getText());
//                if (func.editar(dts)) {
//                    JOptionPane.showConfirmDialog(rootPane, "el empleado fue modificado");
//                }
//            } else {
//                if (func.editarNoContrasena(dts)) {
//                    JOptionPane.showConfirmDialog(rootPane, "el empleado fue modificado");
//                }
//            }
            if (func.editar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "el empleado fue modificado");
                mostrar("");
                limpiar();
                inhabilitar();
            }

        }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        N_Usuario func = new N_Usuario();

        if (!txtIdUsuario.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Desea eliminar el empleado", "confirmar", 2);
            if (confirmacion == 0) {
                func.eliminar(Integer.parseInt(txtIdUsuario.getText()));
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

