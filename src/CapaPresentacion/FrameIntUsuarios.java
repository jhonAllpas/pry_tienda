    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        btnguardar.setText("Editar");
        habilitar();
        btneliminar.setEnabled(true);
        accion = "editar";
        int fila = tablalistado.rowAtPoint(evt.getPoint());

        txtIdUsuario.setText(tablalistado.getValueAt(fila, 0).toString());
        txtIdEmpleado.setText(tablalistado.getValueAt(fila, 1).toString());
        txtUsuario.setText(tablalistado.getValueAt(fila, 2).toString());
        
        txtContrasena.setText(tablalistado.getValueAt(fila, 3).toString());
        cboAcceso.setSelectedIndex(tablalistado.getValueAt(fila, 4).toString().toLowerCase().equals("administrador") ? 0 : 1);
        cboEstado.setSelectedIndex(tablalistado.getValueAt(fila, 5).toString().equals("AC") ? 0 : 1);
    }//GEN-LAST:event_tablalistadoMouseClicked

